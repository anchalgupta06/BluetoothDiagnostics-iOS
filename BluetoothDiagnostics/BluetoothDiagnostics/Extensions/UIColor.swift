import Foundation
import UIKit

extension UIColor {
    static func appGray() -> UIColor {
        return UIColor.systemGray6
    }
    
    static func connectingGray() -> UIColor {
        return UIColor.systemGray
    }
    
    static func notConnectedBlue() -> UIColor {
        return UIColor.blue
    }
    
    static func connectedGreen() -> UIColor {
        return UIColor.green
    }
    
    static func activityIndicatorGray() -> UIColor {
        return UIColor.darkGray
    }
    
    static func buttonTitleBlue() -> UIColor {
        return UIColor.systemBlue
    }
    
    static func subTextGray() -> UIColor {
        return UIColor.darkGray
    }
}
